CREATE TABLE EMP110 (
    eid INT PRIMARY KEY,
    ename VARCHAR(50),
    age INT,
    salary DECIMAL(10,2)
);


CREATE TABLE DEPT110 (
    did INT PRIMARY KEY,
    dname VARCHAR(50),
    budget DECIMAL(10,2),
    managerid INT
);

CREATE TABLE WORK110 (
    eid INT,
    did INT,
    pcdate DATE,
    PRIMARY KEY (eid, did),
    FOREIGN KEY (eid) REFERENCES EMP110(eid),
    FOREIGN KEY (did) REFERENCES DEPT110(did)
);


